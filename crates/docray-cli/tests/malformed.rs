#[test]
fn malformed_pptx_corpus_is_structured_and_never_panics() {
    for (name, code, error_code) in [
        ("zip-bomb.pptx", 4, "parse_failure"),
        ("path-traversal.pptx", 4, "parse_failure"),
        ("truncated.pptx", 4, "parse_failure"),
        ("not-pptx.zip", 2, "unsupported_format"),
        ("legacy-office.cfb", 2, "unsupported_format"),
    ] {
        dps()
            .arg("extract")
            .arg(testdata(&format!("malformed/{name}")))
            .args(["--granularity", "element"])
            .assert()
            .code(code)
            .stderr(predicate::str::contains(format!("\"{error_code}\"")))
            .stderr(predicate::str::contains("panicked").not());
    }
}

