use assert_cmd::Command;
use predicates::prelude::*;

fn testdata(name: &str) -> String {
    format!("{}/../../testdata/{name}", env!("CARGO_MANIFEST_DIR"))
}

fn dps() -> Command {
    let mut cmd = Command::cargo_bin("docray").unwrap();
    cmd.env(
        "DOCRAY_PDFIUM_DIR",
        format!("{}/../../.pdfium/lib", env!("CARGO_MANIFEST_DIR")),
    );
    cmd.timeout(std::time::Duration::from_secs(30));
    cmd
}

// Deviation from the brief: the encrypted fixture lives under testdata/encrypted/
// rather than directly at testdata/encrypted.pdf. The golden harness
// (crates/docray-pdf/tests/golden.rs) globs every *.pdf directly inside testdata/
// and calls PdfExtractor::extract on it unconditionally; extracting an
// encrypted PDF fails, which would break that harness. Putting it in a
// subdirectory keeps it out of that top-level glob.
#[test]
fn encrypted_pdf_exits_3() {
    dps()
        .arg("extract")
        .arg(testdata("encrypted/encrypted.pdf"))
        .assert()
        .code(3)
        .stderr(predicate::str::contains("\"encrypted_pdf\""));
}

