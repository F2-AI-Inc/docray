#[test]
fn truncated_pdf_never_panics() {
    // Either recovers with warnings (exit 0) or fails structurally (exit 4) —
    // both fine; a panic/abort (101/134) is not.
    let assert = dps()
        .arg("extract")
        .arg(testdata("malformed/truncated.pdf"))
        .assert();
    let code = assert.get_output().status.code().unwrap_or(-1);
    assert!(code == 0 || code == 4, "unexpected exit code {code}");
    let stderr = String::from_utf8_lossy(&assert.get_output().stderr);
    assert!(!stderr.contains("panicked"), "extractor panicked: {stderr}");
}

