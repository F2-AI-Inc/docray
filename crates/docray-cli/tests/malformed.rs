// corrupt-page.pdf (generated by gen_fixtures.rs) is a structurally valid
// 3-page PDF whose page 2 content stream bytes were overwritten with 'X'
// after saving. The container (xref, page tree) still parses, so extraction
// succeeds; only page 2's drawable content is unreadable.
//
// Verified behavior: pdfium's content-stream parser recovers *silently* here
// — no warning is produced for page 2, it just yields zero objects for that
// page while pages 1 and 3 (untouched) keep their content. This test asserts
// that actual behavior (empty page 2) but also accepts a page-2 warning,
// in case a future pdfium/pdfium-render upgrade starts surfacing one.
#[test]
fn corrupt_page_content_stream_extracts_with_empty_page_two() {
    let assert = dps()
        .arg("extract")
        .arg(testdata("malformed/corrupt-page.pdf"))
        .assert()
        .success();
    let stdout = String::from_utf8_lossy(&assert.get_output().stdout).into_owned();
    let json: serde_json::Value = serde_json::from_str(&stdout).unwrap();

    assert_eq!(json["document"]["page_count"], 3);
    let pages = json["pages"].as_array().unwrap();
    assert_eq!(pages.len(), 3);

    let elements_of = |i: usize| pages[i]["elements"].as_array().unwrap().len();
    let warnings = json["warnings"].as_array().unwrap();
    let warned_about_page2 = warnings
        .iter()
        .any(|w| w.as_str().unwrap_or("").contains("page 2"));

    assert!(
        elements_of(1) == 0 || warned_about_page2,
        "expected page 2 empty or warned-about; got {} elements, warnings {warnings:?}",
        elements_of(1)
    );
    assert!(elements_of(0) > 0, "page 1 should still have its content");
    assert!(elements_of(2) > 0, "page 3 should still have its content");
}
